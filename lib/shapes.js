class shape{
    constructor(textColor, shapeColor,text){
        this.textColor=textColor;
        this.shapeColor = shapeColor;
        this.text=text;
    }
}

class circle extends shape{
    constructor(textColor, shapeColor, text)
}

class square extends shape{
    constructor(textColor, shapeColor, text)
}

class triangle extends shape{
    constructor(textColor, shapeColor, text)
}

module.exports(shape)